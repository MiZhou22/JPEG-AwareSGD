# The init.py file is used to organize packages. Here, we need to clarify the concept of a package.
# What is a package? Simply put, a package is a folder that contains Python modules. A Python module
# is a .py file that contains functions and classes. A package is designed for better management of modules,
# serving as a parent node for multiple modules.
# In other words, when we import a package, it implicitly executes __init__.py,
# and the objects defined in __init__.py will be bound to the current namespace.
